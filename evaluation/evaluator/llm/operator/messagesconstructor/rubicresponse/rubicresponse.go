type rubicMessagesConstructor struct {
}

func New() messagesconstructor.MessagesConstructor {
	return &rubicMessagesConstructor{}
}

func (e *rubicMessagesConstructor) ConstructMessages(ctx context.Context, actual, _ *evalset.Invocation,
	evalMetric *metric.EvalMetric) ([]model.Message, error) {
	responseSteps, err := content.ExtractIntermediateData(actual.IntermediateData)
	if err != nil {
		return nil, fmt.Errorf("extract intermediate data: %w", err)
	}
	data := rubicResponsePromptData{
		UserInput:     content.ExtractTextFromContent(actual.UserContent),
		ResponseSteps: responseSteps,
		FinalResponse: content.ExtractTextFromContent(actual.FinalResponse),
		Rubrics:       content.ExtractRubrics(evalMetric.Criterion.LLMJudge.Rubrics),
	}
	var buf bytes.Buffer
	if err := rubicResponsePromptTemplate.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("execute rubic response prompt template: %w", err)
	}
	return []model.Message{
		{
			Role:    model.RoleUser,
			Content: buf.String(),
		},
	}, nil
}

type rubicResponsePromptData struct {
	UserInput     string
	ResponseSteps string
	FinalResponse string
	Rubrics       string
}
