// Package promptiter defines shared domain models used by the PromptIter workflow.
package promptiter

import astructure "trpc.group/trpc-go/trpc-agent-go/agent/structure"

// SurfaceGradient carries one sample-local gradient attribution to one surface.
type SurfaceGradient struct {
	// EvalSetID records which evaluation set generated this gradient.
	EvalSetID string
	// EvalCaseID records which evaluation case generated this gradient.
	EvalCaseID string
	// StepID identifies the trace node where the gradient was derived.
	StepID string
	// SurfaceID identifies the concrete surface this gradient should update.
	SurfaceID string
	// Severity preserves the upstream terminal loss importance for weighting when available.
	Severity LossSeverity
	// Gradient stores a serialized gradient expression generated by backwarder.
	Gradient string
}

// StepGradient groups gradients attributed when processing one step.
type StepGradient struct {
	// StepID identifies the owning step in the trace graph.
	StepID string
	// NodeID identifies the node that owns this step in the trace.
	NodeID string
	// Gradients stores surface-level gradients produced by this step.
	Gradients []SurfaceGradient
}

// AggregatedSurfaceGradient stores the merged gradient signal for one surface.
type AggregatedSurfaceGradient struct {
	// SurfaceID is the target surface after sample-level merge.
	SurfaceID string
	// NodeID is the node owning this surface in the static structure.
	NodeID string
	// Type is the surface type used during aggregation and optimization.
	Type astructure.SurfaceType
	// Gradients stores normalized gradients merged across cases.
	Gradients []SurfaceGradient
}
