// Package engine implements PromptIter orchestration and runtime flow for a generation round.
package engine

import (
	"context"

	"trpc.group/trpc-go/trpc-agent-go/evaluation/workflow/promptiter"
)

// EvaluationOptions configures how many traces and parallel branches run in evaluation.
type EvaluationOptions struct {
	// NumRuns controls repeat count for each case to reduce variance.
	NumRuns int
	// EvalCaseParallelism caps parallel case executions per evaluation set.
	EvalCaseParallelism int
	// EvalCaseParallelInferenceEnabled enables concurrent teacher inference calls.
	EvalCaseParallelInferenceEnabled bool
	// EvalCaseParallelEvaluationEnabled enables concurrent judge evaluation tasks.
	EvalCaseParallelEvaluationEnabled bool
}

// MetricResult stores one metric measurement from a single trace step.
type MetricResult struct {
	// MetricName identifies the metric that produced this value.
	MetricName string
	// Score is the numeric result of metric evaluation.
	Score float64
	// Status describes pass/fail or custom metric state.
	Status string
	// Reason stores optional explanation when score quality is degraded.
	Reason string
	// StepID links this metric to the originating trace step.
	StepID string
}

// CaseResult stores output and scoring details of one evaluated case.
type CaseResult struct {
	// EvalSetID identifies the originating evaluation set.
	EvalSetID string
	// EvalCaseID identifies the case sampled from the set.
	EvalCaseID string
	// SessionID is the trace session generated by the test runner.
	SessionID string
	// InvocationID identifies the runner invocation for trace correlation.
	InvocationID string
	// Output is the final textual output returned by the model run.
	Output *promptiter.TraceOutput
	// Trace records the node-level execution path used to produce the output.
	Trace *promptiter.Trace
	// Metrics stores all metric outputs for this case.
	Metrics []MetricResult
}

// EvalSetResult stores aggregate scores and per-case results for one set.
type EvalSetResult struct {
	// EvalSetID identifies the dataset used for this aggregate.
	EvalSetID string
	// OverallScore is the set-level summary score.
	OverallScore float64
	// Cases contains per-case evaluation details.
	Cases []CaseResult
}

// EvaluationResult stores complete evaluation output for one run phase.
type EvaluationResult struct {
	// EvalSets stores results grouped by evaluation set.
	EvalSets []EvalSetResult
}

