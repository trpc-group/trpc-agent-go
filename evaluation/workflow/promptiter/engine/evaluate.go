// EvaluationOptions configures evaluation concurrency behavior.
type EvaluationOptions struct {
	// EvalCaseParallelism caps parallel case executions per evaluation set.
	EvalCaseParallelism int
	// EvalCaseParallelInferenceEnabled enables concurrent teacher inference calls.
	EvalCaseParallelInferenceEnabled bool
	// EvalCaseParallelEvaluationEnabled enables concurrent judge evaluation tasks.
	EvalCaseParallelEvaluationEnabled bool
}

// EvaluationRequest describes one evaluation execution requested by the engine.
type EvaluationRequest struct {
	// EvalSets identifies the evaluation sets and case filters to execute.
	EvalSets []EvalSetInput
	// Profile is the candidate profile being evaluated.
	Profile *promptiter.Profile
	// Teacher is the optional runner used by the evaluation runtime.
	Teacher runner.Runner
	// Judge is the optional runner used by the evaluation runtime.
	Judge runner.Runner
	// Options controls evaluation parallelism.
	Options EvaluationOptions
}

// MetricResult stores one metric measurement for an evaluated case.
type MetricResult struct {
	// MetricName identifies the metric that produced this value.
	MetricName string
	// Score is the numeric result of metric evaluation.
	Score float64
	// Status describes pass/fail or custom metric state.
	Status status.EvalStatus
	// Reason stores optional explanation when score quality is degraded.
	Reason string
}

// CaseResult stores output and scoring details of one evaluated case.
type CaseResult struct {
	// EvalSetID identifies the originating evaluation set.
	EvalSetID string
	// EvalCaseID identifies the case sampled from the set.
	EvalCaseID string
	// SessionID is the trace session generated by the test runner.
	SessionID string
	// Trace records the node-level execution path used to produce the output.
	Trace *atrace.Trace
	// Metrics stores all metric outputs for this case.
	Metrics []MetricResult
	// ActualInvocations contains the optional agent outputs used by metric evaluators.
	// It is omitted from serialized results when run details are unavailable.
	ActualInvocations []*evalset.Invocation `json:"actualInvocations,omitempty"`
	// ExpectedInvocations contains the optional reference outputs paired with
	// ActualInvocations. It is omitted from serialized results when unavailable.
	ExpectedInvocations []*evalset.Invocation `json:"expectedInvocations,omitempty"`
}

// EvalSetResult stores aggregate scores and per-case results for one set.
type EvalSetResult struct {
	// EvalSetID identifies the dataset used for this aggregate.
	EvalSetID string
	// OverallScore is the set-level summary score.
	OverallScore float64
	// Cases contains per-case evaluation details.
	Cases []CaseResult
}

// EvaluationResult stores complete evaluation output for one run phase.
type EvaluationResult struct {
	// OverallScore stores the aggregate score across all evaluation sets.
	OverallScore float64
	// EvalSets stores results grouped by evaluation set.
	EvalSets []EvalSetResult
}

func (e *engine) evaluate(
	ctx context.Context,
	structure *profilecompiler.Structure,
	request *EvaluationRequest,
) (*EvaluationResult, error) {
	if request == nil {
		return nil, errors.New("evaluation request is nil")
	}
	if err := validateEvalSetInputs("", request.EvalSets); err != nil {
		return nil, err
	}
	if e.agentEvaluator == nil {
		return nil, errors.New("agent evaluator is nil")
	}
	runOptions, err := compileProfileRunOptions(structure, request.Profile)
	if err != nil {
		return nil, err
	}
	results := make([]EvalSetResult, 0, len(request.EvalSets))
	totalScore := 0.0
	for _, input := range request.EvalSets {
		options, err := buildEvaluationCallOptions(request, input, runOptions)
		if err != nil {
			return nil, err
		}
		genericResult, err := e.agentEvaluator.Evaluate(ctx, input.EvalSetID, options...)
		if err != nil {
			return nil, err
		}
		evalSetResult, err := adaptEvaluationSetResult(structure, input.EvalSetID, genericResult)
		if err != nil {
			return nil, err
		}
		results = append(results, *evalSetResult)
		totalScore += evalSetResult.OverallScore
	}
	return &EvaluationResult{
		OverallScore: totalScore / float64(len(results)),
		EvalSets:     results,
	}, nil
}

func evaluationScore(result *EvaluationResult) (float64, error) {
	if result == nil {
		return 0, errors.New("evaluation result is nil")
	}
	if len(result.EvalSets) == 0 {
		return 0, errors.New("evaluation result has no eval sets")
	}
	return result.OverallScore, nil
}

func buildEvaluationCallOptions(
	request *EvaluationRequest,
	input EvalSetInput,
	runOptions []agent.RunOption,
) ([]evaluation.Option, error) {
	if request == nil {
		return nil, errors.New("evaluation request is nil")
	}
	options := make([]evaluation.Option, 0, 8)
	options = append(options, evaluation.WithRunDetailsEnabled(true))
	options = append(options, evaluation.WithRunOptions(runOptions...))
	if request.Teacher != nil {
		options = append(options, evaluation.WithExpectedRunner(request.Teacher))
	}
	if request.Judge != nil {
		options = append(options, evaluation.WithJudgeRunner(request.Judge))
	}
	options = append(options, evaluation.WithNumRuns(1))
	if request.Options.EvalCaseParallelism > 0 {
		options = append(options, evaluation.WithEvalCaseParallelism(request.Options.EvalCaseParallelism))
	}
	if request.Options.EvalCaseParallelInferenceEnabled {
		options = append(options, evaluation.WithEvalCaseParallelInferenceEnabled(true))
	}
	if request.Options.EvalCaseParallelEvaluationEnabled {
		options = append(options, evaluation.WithEvalCaseParallelEvaluationEnabled(true))
	}
	if len(input.EvalCaseIDs) > 0 {
		options = append(options, evaluation.WithEvalCaseIDs(input.EvalCaseIDs...))
	}
	return options, nil
}

func compileProfileRunOptions(
	structure *profilecompiler.Structure,
	profile *promptiter.Profile,
) ([]agent.RunOption, error) {
	compilerProfile, err := structure.NormalizeProfile(toCompilerProfile(profile))
	if err != nil {
		return nil, err
	}
	runOptions, err := profilecompiler.CompileRunOptions(compilerProfile, true)
	if err != nil {
		return nil, err
	}
	if len(compilerProfile.Overrides) > 0 {
		runOptions = append(runOptions, profilecompiler.WithProfile(compilerProfile))
	}
	return runOptions, nil
}

func adaptEvaluationSetResult(
	structure *profilecompiler.Structure,
	evalSetID string,
	result *evaluation.EvaluationResult,
) (*EvalSetResult, error) {
	if result == nil {
		return nil, errors.New("evaluation result is nil")
	}
	if result.EvalSetID == "" {
		return nil, errors.New("evaluation result eval set id is empty")
	}
	if result.EvalSetID != evalSetID {
		return nil, fmt.Errorf(
			"evaluation result eval set id %q does not match request %q",
			result.EvalSetID,
			evalSetID,
		)
	}
	score, err := calculateEvaluationScore(result)
	if err != nil {
		return nil, err
	}
	cases := make([]CaseResult, 0, len(result.EvalCases))
	for _, evalCase := range result.EvalCases {
		if evalCase == nil {
			continue
		}
		converted, err := adaptEvaluationCaseResult(structure, evalSetID, evalCase)
		if err != nil {
			return nil, err
		}
		cases = append(cases, *converted)
	}
	return &EvalSetResult{
		EvalSetID:    evalSetID,
		OverallScore: score,
		Cases:        cases,
	}, nil
}

func calculateEvaluationScore(result *evaluation.EvaluationResult) (float64, error) {
	if result == nil {
		return 0, errors.New("evaluation result is nil")
	}
	totalScore := 0.0
	totalMetrics := 0
	for _, evalCase := range result.EvalCases {
		if evalCase == nil {
			continue
		}
		for _, metric := range evalCase.MetricResults {
			if metric == nil || metric.EvalStatus == status.EvalStatusNotEvaluated {
				continue
			}
			totalScore += metric.Score
			totalMetrics++
		}
	}
	if totalMetrics == 0 {
		return 0, errors.New("evaluation result has no metric scores")
	}
	return totalScore / float64(totalMetrics), nil
}

func adaptEvaluationCaseResult(
	structure *profilecompiler.Structure,
	evalSetID string,
	evalCase *evaluation.EvaluationCaseResult,
) (*CaseResult, error) {
	if evalCase == nil {
		return nil, errors.New("evaluation case result is nil")
	}
	if evalCase.EvalCaseID == "" {
		return nil, errors.New("evaluation case id is empty")
	}
	if len(evalCase.EvalCaseResults) == 0 {
		return nil, fmt.Errorf(
			"evaluation case %q has no run results",
			evalCase.EvalCaseID,
		)
	}
	runResult := evalCase.EvalCaseResults[0]
	if runResult == nil {
		return nil, fmt.Errorf("evaluation case %q run result is nil", evalCase.EvalCaseID)
	}
	if len(evalCase.RunDetails) == 0 {
		return nil, fmt.Errorf(
			"evaluation case %q has no run details",
			evalCase.EvalCaseID,
		)
	}
	runDetail := evalCase.RunDetails[0]
	if runDetail == nil {
		return nil, fmt.Errorf("evaluation case %q run detail is nil", evalCase.EvalCaseID)
	}
	if runDetail.RunID != runResult.RunID {
		return nil, fmt.Errorf(
			"evaluation case %q run detail id %d does not match run result id %d",
			evalCase.EvalCaseID,
			runDetail.RunID,
			runResult.RunID,
		)
	}
	trace, sessionID, err := extractInferenceTraceDetails(structure, evalCase.EvalCaseID, runDetail.Inference)
	if err != nil {
		return nil, fmt.Errorf(
			"extract inference trace details for eval case %q: %w",
			evalCase.EvalCaseID,
			err,
		)
	}
	metrics, err := adaptMetricResults(runResult.OverallEvalMetricResults)
	if err != nil {
		return nil, fmt.Errorf(
			"adapt metric results for eval case %q: %w",
			evalCase.EvalCaseID,
			err,
		)
	}
	return &CaseResult{
		EvalSetID:           evalSetID,
		EvalCaseID:          evalCase.EvalCaseID,
		SessionID:           sessionID,
		Trace:               trace,
		Metrics:             metrics,
		ActualInvocations:   append([]*evalset.Invocation(nil), runDetail.Inference.Inferences...),
		ExpectedInvocations: expectedInvocations(runResult),
	}, nil
}

func expectedInvocations(result *evalresult.EvalCaseResult) []*evalset.Invocation {
	if result == nil {
		return nil
	}
	expected := make([]*evalset.Invocation, len(result.EvalMetricResultPerInvocation))
	hasEvidence := false
	for i, perInvocation := range result.EvalMetricResultPerInvocation {
		if perInvocation != nil && perInvocation.ExpectedInvocation != nil {
			expected[i] = perInvocation.ExpectedInvocation
			hasEvidence = true
		}
	}
	if !hasEvidence {
		return nil
	}
	return expected
}

func extractInferenceTraceDetails(
	structure *profilecompiler.Structure,
	evalCaseID string,
	result *evaluation.EvaluationInferenceDetails,
) (*atrace.Trace, string, error) {
	if structure == nil {
		return nil, "", errors.New("structure is nil")
	}
	if result == nil {
		return nil, "", errors.New("inference result is nil")
	}
	if len(result.ExecutionTraces) != 1 {
		return nil, "", fmt.Errorf(
			"inference result for eval case %q must contain exactly one execution trace",
			evalCaseID,
		)
	}
	executionTrace := result.ExecutionTraces[0]
	if err := validateTrace(structure, executionTrace); err != nil {
		return nil, "", err
	}
	sessionID := executionTrace.SessionID
	if sessionID == "" {
		sessionID = result.SessionID
	}
	return executionTrace, sessionID, nil
}

func validateTrace(structure *profilecompiler.Structure, trace *atrace.Trace) error {
	if structure == nil {
		return errors.New("structure is nil")
	}
	if trace == nil {
		return errors.New("execution trace is nil")
	}
	for _, step := range trace.Steps {
		if step.StepID == "" {
			return errors.New("execution trace step id is empty")
		}
		for _, surfaceID := range step.AppliedSurfaceIDs {
			if surfaceID == "" {
				return fmt.Errorf("execution trace step %q applied surface id is empty", step.StepID)
			}
			if _, ok := structure.KnownSurfaceIDs[surfaceID]; !ok {
				return fmt.Errorf(
					"execution trace step %q references unknown surface id %q",
					step.StepID,
					surfaceID,
				)
			}
		}
	}
	return nil
}

func adaptMetricResults(results []*evalresult.EvalMetricResult) ([]MetricResult, error) {
	metrics := make([]MetricResult, 0, len(results))
	for _, result := range results {
		if result == nil {
			continue
		}
		metric := MetricResult{
			MetricName: result.MetricName,
			Score:      result.Score,
			Status:     result.EvalStatus,
		}
		if result.Details != nil {
			metric.Reason = strings.TrimSpace(result.Details.Reason)
		}
		if metric.Status == status.EvalStatusFailed && metric.Reason == "" {
			return nil, fmt.Errorf("metric %q is missing loss reason", metric.MetricName)
		}
		metrics = append(metrics, metric)
	}
	return metrics, nil
}
