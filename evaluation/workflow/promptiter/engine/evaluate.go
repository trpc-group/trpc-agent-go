// EvaluationOptions configures evaluation concurrency behavior.
type EvaluationOptions struct {
	// NumRuns controls repeated evaluation executions per case. Zero defaults to one.
	NumRuns int
	// TraceUsageCoversAllCalls is true only when every model-bearing call made
	// by inference and metric evaluation is represented by retained execution
	// trace usage. It must remain false for LLM judges, expected runners, or
	// custom evaluators whose telemetry is not included in those traces.
	TraceUsageCoversAllCalls bool
	// EvalCaseParallelism caps parallel case executions per evaluation set.
	EvalCaseParallelism int
	// EvalCaseParallelInferenceEnabled enables concurrent teacher inference calls.
	EvalCaseParallelInferenceEnabled bool
	// EvalCaseParallelEvaluationEnabled enables concurrent judge evaluation tasks.
	EvalCaseParallelEvaluationEnabled bool
}

// EvaluationRequest describes one evaluation execution requested by the engine.
type EvaluationRequest struct {
	// EvalSets identifies the evaluation sets and case filters to execute.
	EvalSets []EvalSetInput
	// Profile is the candidate profile being evaluated.
	Profile *promptiter.Profile
	// Teacher is the optional runner used by the evaluation runtime.
	Teacher runner.Runner
	// Judge is the optional runner used by the evaluation runtime.
	Judge runner.Runner
	// Options controls evaluation parallelism.
	Options EvaluationOptions
}

// MetricResult stores one metric measurement for an evaluated case.
type MetricResult struct {
	// MetricName identifies the metric that produced this value.
	MetricName string
	// Score is the numeric result of metric evaluation.
	Score float64
	// Threshold is the configured pass threshold.
	Threshold float64
	// Status describes pass/fail or custom metric state.
	Status status.EvalStatus
	// Reason stores optional explanation when score quality is degraded.
	Reason string
	// Details preserves structured rubric evidence for audit and attribution.
	Details *evalresult.EvalMetricResultDetails
}

// CaseResult stores output and scoring details of one evaluated case.
type CaseResult struct {
	// EvalSetID identifies the originating evaluation set.
	EvalSetID string
	// EvalCaseID identifies the case sampled from the set.
	EvalCaseID string
	// SessionID is the trace session generated by the first test run.
	SessionID string
	// Trace records the first node-level execution path used by backward propagation.
	Trace *atrace.Trace
	// RunDetails preserves every repeated inference observation for audit and attribution.
	RunDetails []*evaluation.EvaluationCaseRunDetails
	// RunResults preserves per-run metric outcomes for stability analysis.
	RunResults []*evalresult.EvalCaseResult
	// Metrics stores all metric outputs for this case.
	Metrics []MetricResult
}

// EvalSetResult stores aggregate scores and per-case results for one set.
type EvalSetResult struct {
	// EvalSetID identifies the dataset used for this aggregate.
	EvalSetID string
	// OverallScore is the set-level summary score.
	OverallScore float64
	// Cases contains per-case evaluation details.
	Cases []CaseResult
	// Usage contains model-call telemetry observed for this evaluation set.
	Usage promptiter.Usage
}

// EvaluationResult stores complete evaluation output for one run phase.
type EvaluationResult struct {
	// OverallScore stores the aggregate score across all evaluation sets.
	OverallScore float64
	// EvalSets stores results grouped by evaluation set.
	EvalSets []EvalSetResult
	// Usage contains model-call telemetry across all evaluation sets.
	Usage promptiter.Usage
}

func (e *engine) evaluate(
	ctx context.Context,
	structure *profilecompiler.Structure,
	request *EvaluationRequest,
) (*EvaluationResult, error) {
	if request == nil {
		return nil, errors.New("evaluation request is nil")
	}
	if err := validateEvalSetInputs("", request.EvalSets); err != nil {
		return nil, err
	}
	if e.agentEvaluator == nil {
		return nil, errors.New("agent evaluator is nil")
	}
	runOptions, err := compileProfileRunOptions(structure, request.Profile)
	if err != nil {
		return nil, err
	}
	results := make([]EvalSetResult, 0, len(request.EvalSets))
	usages := make([]promptiter.Usage, 0, len(request.EvalSets))
	totalScore := 0.0
	for _, input := range request.EvalSets {
		options, err := buildEvaluationCallOptions(request, input, runOptions)
		if err != nil {
			return nil, err
		}
		genericResult, err := e.agentEvaluator.Evaluate(ctx, input.EvalSetID, options...)
		if err != nil {
			return nil, err
		}
		evalSetResult, err := adaptEvaluationSetResult(structure, input.EvalSetID, genericResult)
		if err != nil {
			return nil, err
		}
		results = append(results, *evalSetResult)
		usages = append(usages, evalSetResult.Usage)
		totalScore += evalSetResult.OverallScore
	}
	usage := promptiter.MergeUsage(usages...)
	// Expected and judge runners may execute model calls outside the retained
	// inference traces. Until Evaluation exposes their telemetry explicitly,
	// the aggregate must remain incomplete.
	if !request.Options.TraceUsageCoversAllCalls || request.Teacher != nil || request.Judge != nil {
		usage.Complete = false
	}
	return &EvaluationResult{
		OverallScore: totalScore / float64(len(results)),
		EvalSets:     results,
		Usage:        usage,
	}, nil
}

func evaluationScore(result *EvaluationResult) (float64, error) {
	if result == nil {
		return 0, errors.New("evaluation result is nil")
	}
	if len(result.EvalSets) == 0 {
		return 0, errors.New("evaluation result has no eval sets")
	}
	return result.OverallScore, nil
}

func buildEvaluationCallOptions(
	request *EvaluationRequest,
	input EvalSetInput,
	runOptions []agent.RunOption,
) ([]evaluation.Option, error) {
	if request == nil {
		return nil, errors.New("evaluation request is nil")
	}
	options := make([]evaluation.Option, 0, 8)
	options = append(options, evaluation.WithRunDetailsEnabled(true))
	options = append(options, evaluation.WithRunOptions(runOptions...))
	if request.Teacher != nil {
		options = append(options, evaluation.WithExpectedRunner(request.Teacher))
	}
	if request.Judge != nil {
		options = append(options, evaluation.WithJudgeRunner(request.Judge))
	}
	numRuns := request.Options.NumRuns
	if numRuns <= 0 {
		numRuns = 1
	}
	options = append(options, evaluation.WithNumRuns(numRuns))
	if request.Options.EvalCaseParallelism > 0 {
		options = append(options, evaluation.WithEvalCaseParallelism(request.Options.EvalCaseParallelism))
	}
	if request.Options.EvalCaseParallelInferenceEnabled {
		options = append(options, evaluation.WithEvalCaseParallelInferenceEnabled(true))
	}
	if request.Options.EvalCaseParallelEvaluationEnabled {
		options = append(options, evaluation.WithEvalCaseParallelEvaluationEnabled(true))
	}
	if len(input.EvalCaseIDs) > 0 {
		options = append(options, evaluation.WithEvalCaseIDs(input.EvalCaseIDs...))
	}
	return options, nil
}

func compileProfileRunOptions(
	structure *profilecompiler.Structure,
	profile *promptiter.Profile,
) ([]agent.RunOption, error) {
	compilerProfile, err := structure.NormalizeProfile(toCompilerProfile(profile))
	if err != nil {
		return nil, err
	}
	runOptions, err := profilecompiler.CompileRunOptions(compilerProfile, true)
	if err != nil {
		return nil, err
	}
	if len(compilerProfile.Overrides) > 0 {
		runOptions = append(runOptions, profilecompiler.WithProfile(compilerProfile))
	}
	return runOptions, nil
}

func adaptEvaluationSetResult(
	structure *profilecompiler.Structure,
	evalSetID string,
	result *evaluation.EvaluationResult,
) (*EvalSetResult, error) {
	if result == nil {
		return nil, errors.New("evaluation result is nil")
	}
	if result.EvalSetID == "" {
		return nil, errors.New("evaluation result eval set id is empty")
	}
	if result.EvalSetID != evalSetID {
		return nil, fmt.Errorf(
			"evaluation result eval set id %q does not match request %q",
			result.EvalSetID,
			evalSetID,
		)
	}
	score, err := calculateEvaluationScore(result)
	if err != nil {
		return nil, err
	}
	cases := make([]CaseResult, 0, len(result.EvalCases))
	for _, evalCase := range result.EvalCases {
		if evalCase == nil {
			continue
		}
		converted, err := adaptEvaluationCaseResult(structure, evalSetID, evalCase)
		if err != nil {
			return nil, err
		}
		cases = append(cases, *converted)
	}
	return &EvalSetResult{
		EvalSetID:    evalSetID,
		OverallScore: score,
		Cases:        cases,
		Usage:        summarizeEvaluationUsage(result),
	}, nil
}

func summarizeEvaluationUsage(result *evaluation.EvaluationResult) promptiter.Usage {
	if result == nil {
		return promptiter.Usage{}
	}
	values := make([]promptiter.Usage, 0)
	for _, evalCase := range result.EvalCases {
		if evalCase == nil || len(evalCase.RunDetails) == 0 {
			values = append(values, promptiter.Usage{})
			continue
		}
		for _, run := range evalCase.RunDetails {
			if run == nil || run.Inference == nil || len(run.Inference.ExecutionTraces) == 0 {
				values = append(values, promptiter.Usage{})
				continue
			}
			for _, executionTrace := range run.Inference.ExecutionTraces {
				values = append(values, summarizeTraceUsage(executionTrace))
			}
		}
	}
	if len(values) == 0 {
		return promptiter.Usage{}
	}
	return promptiter.MergeUsage(values...)
}

func summarizeTraceUsage(executionTrace *atrace.Trace) promptiter.Usage {
	if executionTrace == nil {
		return promptiter.Usage{}
	}
	result := promptiter.Usage{}
	for _, step := range executionTrace.Steps {
		if step.Usage == nil {
			continue
		}
		result.Calls++
	}
	if executionTrace.Usage != nil {
		if result.Calls == 0 {
			result.Calls = 1
		}
		result.PromptTokens = int64(executionTrace.Usage.PromptTokens)
		result.CompletionTokens = int64(executionTrace.Usage.CompletionTokens)
		result.TotalTokens = int64(executionTrace.Usage.TotalTokens)
		result.Complete = true
		return result
	}
	for _, step := range executionTrace.Steps {
		if step.Usage == nil {
			continue
		}
		result.PromptTokens += int64(step.Usage.PromptTokens)
		result.CompletionTokens += int64(step.Usage.CompletionTokens)
		result.TotalTokens += int64(step.Usage.TotalTokens)
	}
	// Step usage can provide useful partial totals, but the trace model does not
	// identify which steps were model-bearing. Without trace-level aggregate
	// usage, missing provider telemetry cannot be distinguished from non-model
	// steps, so completeness must remain false.
	result.Complete = false
	return result
}

func calculateEvaluationScore(result *evaluation.EvaluationResult) (float64, error) {
	if result == nil {
		return 0, errors.New("evaluation result is nil")
	}
	totalScore := 0.0
	totalMetrics := 0
	for _, evalCase := range result.EvalCases {
		if evalCase == nil {
			continue
		}
		for _, metric := range evalCase.MetricResults {
			if metric == nil || metric.EvalStatus == status.EvalStatusNotEvaluated {
				continue
			}
			totalScore += metric.Score
			totalMetrics++
		}
	}
	if totalMetrics == 0 {
		return 0, errors.New("evaluation result has no metric scores")
	}
	return totalScore / float64(totalMetrics), nil
}

func adaptEvaluationCaseResult(
	structure *profilecompiler.Structure,
	evalSetID string,
	evalCase *evaluation.EvaluationCaseResult,
) (*CaseResult, error) {
	if evalCase == nil {
		return nil, errors.New("evaluation case result is nil")
	}
	if evalCase.EvalCaseID == "" {
		return nil, errors.New("evaluation case id is empty")
	}
	if len(evalCase.EvalCaseResults) == 0 {
		return nil, fmt.Errorf(
			"evaluation case %q has no run results",
			evalCase.EvalCaseID,
		)
	}
	if len(evalCase.RunDetails) == 0 {
		return nil, fmt.Errorf(
			"evaluation case %q has no run details",
			evalCase.EvalCaseID,
		)
	}
	_, runDetail, err := representativeRunEvidence(evalCase)
	if err != nil {
		return nil, fmt.Errorf("select representative run for eval case %q: %w", evalCase.EvalCaseID, err)
	}
	trace, sessionID, err := extractInferenceTraceDetails(
		structure, evalCase.EvalCaseID, runDetail.Inference,
	)
	if err != nil {
		return nil, fmt.Errorf(
			"extract inference trace details for eval case %q: %w",
			evalCase.EvalCaseID,
			err,
		)
	}
	// MetricResults is the Evaluation Service's aggregate across all configured
	// runs. PromptIter loss extraction, acceptance, regression deltas, and the
	// human report must use this same evidence. Per-run results remain available
	// separately for stability analysis and audit detail.
	metrics, err := adaptMetricResults(aggregateMetricEvidence(
		evalCase.MetricResults,
		evalCase.EvalCaseResults,
	))
	if err != nil {
		return nil, fmt.Errorf(
			"adapt metric results for eval case %q: %w",
			evalCase.EvalCaseID,
			err,
		)
	}
	return &CaseResult{
		EvalSetID:  evalSetID,
		EvalCaseID: evalCase.EvalCaseID,
		SessionID:  sessionID,
		Trace:      trace,
		RunDetails: append([]*evaluation.EvaluationCaseRunDetails(nil), evalCase.RunDetails...),
		RunResults: append([]*evalresult.EvalCaseResult(nil), evalCase.EvalCaseResults...),
		Metrics:    metrics,
	}, nil
}

func representativeRunEvidence(
	evalCase *evaluation.EvaluationCaseResult,
) (*evalresult.EvalCaseResult, *evaluation.EvaluationCaseRunDetails, error) {
	detailsByRunID := make(map[int]*evaluation.EvaluationCaseRunDetails, len(evalCase.RunDetails))
	for index, detail := range evalCase.RunDetails {
		if detail == nil {
			return nil, nil, fmt.Errorf("run detail at index %d is nil", index)
		}
		if detail.RunID <= 0 {
			return nil, nil, fmt.Errorf("run detail at index %d has invalid id %d", index, detail.RunID)
		}
		if _, exists := detailsByRunID[detail.RunID]; exists {
			return nil, nil, fmt.Errorf("duplicate run detail id %d", detail.RunID)
		}
		detailsByRunID[detail.RunID] = detail
	}
	failedMetrics := make(map[string]struct{})
	for _, metric := range evalCase.MetricResults {
		if metric != nil && metric.EvalStatus == status.EvalStatusFailed {
			failedMetrics[metric.MetricName] = struct{}{}
		}
	}
	seenRuns := make(map[int]struct{}, len(evalCase.EvalCaseResults))
	var fallback *evalresult.EvalCaseResult
	for index, run := range evalCase.EvalCaseResults {
		if run == nil {
			return nil, nil, fmt.Errorf("run result at index %d is nil", index)
		}
		if run.RunID <= 0 {
			return nil, nil, fmt.Errorf("run result at index %d has invalid id %d", index, run.RunID)
		}
		if _, exists := seenRuns[run.RunID]; exists {
			return nil, nil, fmt.Errorf("duplicate run result id %d", run.RunID)
		}
		seenRuns[run.RunID] = struct{}{}
		detail := detailsByRunID[run.RunID]
		if detail == nil {
			return nil, nil, fmt.Errorf("run result id %d has no matching run detail", run.RunID)
		}
		if fallback == nil {
			fallback = run
		}
		if runFailsAnyMetric(run, failedMetrics) {
			return run, detail, nil
		}
	}
	if fallback == nil {
		return nil, nil, errors.New("evaluation case has no non-nil run result")
	}
	if len(detailsByRunID) != len(seenRuns) {
		return nil, nil, fmt.Errorf(
			"run detail count %d does not match run result count %d",
			len(detailsByRunID), len(seenRuns),
		)
	}
	return fallback, detailsByRunID[fallback.RunID], nil
}

func runFailsAnyMetric(
	run *evalresult.EvalCaseResult,
	failedMetrics map[string]struct{},
) bool {
	if run == nil {
		return false
	}
	if run.ErrorMessage != "" {
		return true
	}
	for _, metric := range run.OverallEvalMetricResults {
		if metric == nil || metric.EvalStatus != status.EvalStatusFailed {
			continue
		}
		if len(failedMetrics) == 0 {
			return true
		}
		if _, exists := failedMetrics[metric.MetricName]; exists {
			return true
		}
	}
	return false
}

func aggregateMetricEvidence(
	aggregated []*evalresult.EvalMetricResult,
	runs []*evalresult.EvalCaseResult,
) []*evalresult.EvalMetricResult {
	result := make([]*evalresult.EvalMetricResult, 0, len(aggregated))
	for _, metric := range aggregated {
		if metric == nil {
			continue
		}
		cloned := *metric
		cloned.Details = cloneMetricDetails(metric.Details)
		if cloned.EvalStatus == status.EvalStatusFailed &&
			(cloned.Details == nil || strings.TrimSpace(cloned.Details.Reason) == "") {
			cloned.Details = representativeMetricDetails(runs, cloned.MetricName)
			if cloned.Details == nil || strings.TrimSpace(cloned.Details.Reason) == "" {
				cloned.Details = &evalresult.EvalMetricResultDetails{
					Reason: fmt.Sprintf(
						"aggregate score %.6f is below threshold %.6f across repeated runs",
						cloned.Score,
						cloned.Threshold,
					),
					Score: cloned.Score,
				}
			}
		}
		result = append(result, &cloned)
	}
	return result
}

func representativeMetricDetails(
	runs []*evalresult.EvalCaseResult,
	metricName string,
) *evalresult.EvalMetricResultDetails {
	for _, run := range runs {
		if run == nil {
			continue
		}
		for _, metric := range run.OverallEvalMetricResults {
			if metric == nil || metric.MetricName != metricName ||
				metric.EvalStatus != status.EvalStatusFailed || metric.Details == nil ||
				strings.TrimSpace(metric.Details.Reason) == "" {
				continue
			}
			return cloneMetricDetails(metric.Details)
		}
	}
	return nil
}

func cloneMetricDetails(
	source *evalresult.EvalMetricResultDetails,
) *evalresult.EvalMetricResultDetails {
	if source == nil {
		return nil
	}
	result := *source
	if len(source.RubricScores) > 0 {
		result.RubricScores = make([]*evalresult.RubricScore, 0, len(source.RubricScores))
		for _, rubric := range source.RubricScores {
			if rubric == nil {
				result.RubricScores = append(result.RubricScores, nil)
				continue
			}
			cloned := *rubric
			result.RubricScores = append(result.RubricScores, &cloned)
		}
	}
	return &result
}

func extractInferenceTraceDetails(
	structure *profilecompiler.Structure,
	evalCaseID string,
	result *evaluation.EvaluationInferenceDetails,
) (*atrace.Trace, string, error) {
	if structure == nil {
		return nil, "", errors.New("structure is nil")
	}
	if result == nil {
		return nil, "", errors.New("inference result is nil")
	}
	if len(result.ExecutionTraces) != 1 {
		return nil, "", fmt.Errorf(
			"inference result for eval case %q must contain exactly one execution trace",
			evalCaseID,
		)
	}
	executionTrace := result.ExecutionTraces[0]
	if err := validateTrace(structure, executionTrace); err != nil {
		return nil, "", err
	}
	sessionID := executionTrace.SessionID
	if sessionID == "" {
		sessionID = result.SessionID
	}
	return executionTrace, sessionID, nil
}

func validateTrace(structure *profilecompiler.Structure, trace *atrace.Trace) error {
	if structure == nil {
		return errors.New("structure is nil")
	}
	if trace == nil {
		return errors.New("execution trace is nil")
	}
	for _, step := range trace.Steps {
		if step.StepID == "" {
			return errors.New("execution trace step id is empty")
		}
		for _, surfaceID := range step.AppliedSurfaceIDs {
			if surfaceID == "" {
				return fmt.Errorf("execution trace step %q applied surface id is empty", step.StepID)
			}
			if _, ok := structure.KnownSurfaceIDs[surfaceID]; !ok {
				return fmt.Errorf(
					"execution trace step %q references unknown surface id %q",
					step.StepID,
					surfaceID,
				)
			}
		}
	}
	return nil
}

func adaptMetricResults(results []*evalresult.EvalMetricResult) ([]MetricResult, error) {
	metrics := make([]MetricResult, 0, len(results))
	for _, result := range results {
		if result == nil {
			continue
		}
		metric := MetricResult{
			MetricName: result.MetricName,
			Score:      result.Score,
			Threshold:  result.Threshold,
			Status:     result.EvalStatus,
			Details:    result.Details,
		}
		if result.Details != nil {
			metric.Reason = strings.TrimSpace(result.Details.Reason)
		}
		if metric.Status == status.EvalStatusFailed && metric.Reason == "" {
			return nil, fmt.Errorf("metric %q is missing loss reason", metric.MetricName)
		}
		metrics = append(metrics, metric)
	}
	return metrics, nil
}
